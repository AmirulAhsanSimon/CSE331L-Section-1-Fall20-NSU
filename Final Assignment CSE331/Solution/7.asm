.MODEL SMALL 
.STACK 100H
 
.DATA

NUM1 DB ?
NUM2 DB ?
RESULT DB ?
MSG1 DB 10,13,"Enter the first number to multiply : $"
MSG2 DB 10,13,"Enter the second number to multiply : $"
MSG3 DB 10,13,"Result of multiplication : $"

.CODE
MAIN PROC
ASSUME DS:DATA CS:CODE
MOV AX,DATA
MOV DS,AX
LEA DX,MSG1
MOV AH,9
INT 21H
MOV AH,1
INT 21H
SUB AL,30H
MOV NUM1,AL
LEA DX,MSG2
MOV AH,9
INT 21H
MOV AH,1
INT 21H
SUB AL,30H
MOV NUM2,AL
MUL NUM1
MOV RESULT,AL
ADD AH,30H
ADD AL,30H
MOV BX,AX
LEA DX,MSG3
MOV AH,9
INT 21H
MOV AH,2
MOV DL,BH
INT 21H
MOV AH,2
MOV DL,BL
INT 21H
MOV AH,4CH
INT 21H
MOV CX,15
CMP BX,CX
JG Greater

Greater:MOV DL,1
MOV DL,'A' 
MOV AH,2
INT 21H
JL Less
Less:
MOV DL,0
MOV DL,'A' 
MOV AH,2
INT 21H

   
MAIN ENDP 
END MAIN